import { writeFile } from "node:fs/promises";
import type { CompiledOntology } from "../ontology/compiler.js";
import type { RenderedDoc } from "../harness/doc-renderer.js";

const GENERATED_MARKER = "<!-- generated by ontoraness | do not edit manually -->";

export interface ClaudeCodeAdapterOptions {
  /** 출력 경로 (기본: CLAUDE.md) */
  outputPath?: string;
  /** docs 디렉토리 경로 (인덱스 링크 생성용) */
  docsDir?: string;
  /** harness.config.yml의 append 내용 */
  append?: string;
}

/**
 * 컴파일된 온톨로지에서 CLAUDE.md를 생성한다.
 *
 * 설계 원칙:
 * - Tier1 규칙(절대 제약) + 아키텍처 개요만 포함 (~200 tokens)
 * - 세부 내용은 docs/*.md 인덱스 링크로 대체
 * - 길수록 규칙 희석 → 항상 짧게 유지
 */
export function renderClaudeMd(
  compiled: CompiledOntology,
  docs: RenderedDoc[],
  opts: ClaudeCodeAdapterOptions = {}
): string {
  const { docsDir = ".ontoraness/docs", append } = opts;
  const lines: string[] = [GENERATED_MARKER, ""];

  // 프로젝트 정보
  const archOnto = compiled.sources.find((o) => o.kind === "ArchitectureOntology");
  const agentOnto = compiled.sources.find((o) => o.kind === "AgentOntology");

  // 제목: 첫 번째 온톨로지의 프로젝트명 or 기본값
  const projectName = archOnto?.metadata.name ?? "프로젝트";
  lines.push(`# ${projectName}`);
  lines.push("");

  // 아키텍처 개요 (3-5줄)
  if (archOnto?.kind === "ArchitectureOntology") {
    const { spec } = archOnto;
    if (spec.layers?.length) {
      const layerFlow = spec.layers.map((l) => l.name).join(" → ");
      lines.push("## 아키텍처");
      lines.push(layerFlow);

      const constraints = spec.constraints ?? [];
      const tier1ArchRules = spec.rules.filter((r) => r.enforcement === "tier1");
      [...constraints, ...tier1ArchRules.map((r) => r.description)].forEach(
        (c) => lines.push(`- ${c}`)
      );
      lines.push("");
    }
  }

  // 절대 금지 (Tier1 규칙 전체 — 짧게)
  const neverRules = compiled.tier1Rules.filter(
    (r) => r.severity === "error"
  );
  if (neverRules.length > 0) {
    lines.push("## 절대 금지");
    neverRules.forEach((r) => lines.push(`- ${r.description}`));
    lines.push("");
  }

  // AgentOntology Tier1 전역 제약 (중복 제거)
  if (agentOnto?.kind === "AgentOntology") {
    const agentTier1 = agentOnto.spec.rules.filter(
      (r) => r.enforcement === "tier1" && !neverRules.find((n) => n.id === r.id)
    );
    if (agentTier1.length > 0) {
      agentTier1.forEach((r) => {
        const isNever = r.tags?.includes("never");
        lines.push(`- ${isNever ? "❌" : "✅"} ${r.description}`);
      });
      lines.push("");
    }
  }

  // docs 인덱스
  if (docs.length > 0) {
    lines.push("## 세부 문서 (하네스가 작업별 자동 주입)");
    for (const doc of docs) {
      const onto = compiled.byId.get(doc.id);
      const label = onto?.metadata.name ?? doc.id;
      lines.push(`- ${label}: \`${docsDir}/${doc.filename}\``);
    }
    lines.push("");
  }

  // 수동 추가 섹션 (harness.config.yml append)
  if (append?.trim()) {
    lines.push(append.trim());
    lines.push("");
  }

  return lines.join("\n");
}

/**
 * CLAUDE.md를 파일로 쓴다.
 */
export async function writeClaudeMd(
  content: string,
  outputPath: string = "CLAUDE.md"
): Promise<void> {
  await writeFile(outputPath, content, "utf-8");
}
