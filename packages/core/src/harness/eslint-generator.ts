/** 정규식 패턴 문자열을 ESLint selector로 변환 (단순 변환) */
function patternToSelector(pattern: string): string | null {
  // console.log → CallExpression
  if (pattern.includes("console.log")) {
    return "CallExpression[callee.object.name='console'][callee.property.name='log']";
  }
  if (pattern.includes("console")) {
    return "CallExpression[callee.object.name='console']";
  }
  if (pattern.includes("eval(")) {
    return "CallExpression[callee.name='eval']";
  }
  // 그 외 패턴은 지원하지 않음
  return null;
}

/** 온톨로지 rule.id → ESLint 규칙 매핑 */
function mapOntologyRuleToEslint(
  ruleId: string
): { name: string; options?: unknown[] } | null {
  const mappings: Record<string, { name: string; options?: unknown[] }> = {
    "no-console": { name: "no-console" },
    "no-eval": { name: "no-eval" },
    "no-debugger": { name: "no-debugger" },
    "eqeqeq": { name: "eqeqeq", options: ["always"] },
    "no-var": { name: "no-var" },
    "prefer-const": { name: "prefer-const" },
  };

  // ruleId에서 키워드 감지
  for (const [key, value] of Object.entries(mappings)) {
    if (ruleId.includes(key) || ruleId.includes(key.replace(/-/g, "_"))) {
      return value;
    }
  }

  return null;
}
